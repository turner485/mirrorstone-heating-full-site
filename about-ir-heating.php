                        <span class="orange-line"></span>
                        <img src="/images/about-IR/liverroom-new.png" class="img-responsive"/>
                        <p>
                            Infrared works in a fundamentally different way to convection heaters. Convection radiators heat up the air around them, and create what is known as a convection current –
                            this is simply hot and cold air circulating as the respective temperatures of each change.</p>
                        <p> What this also does is circulate dust and germs around a room, which isn’t good for the 5.4 million people who suffer from respiratory issues in the UK. That aside,
                            it also creates a hugely stuffy atmosphere which can become uncomfortable.</p>

                        <p>Infrared changes everything. The waves travel from the panel until they hit an object – this could be a wall, a piece of furniture, or even a person. The object absorbs some of the energy, whilst the rest of the waves bounce off.
                            This process repeats until all the energy in the waves has been exhausted.</p>
                        <p>The key difference is that infrared works by heating objects and surface area, whilst convection heaters heat volumes of air.
                            This brings many advantages – it takes far more energy to heat air than it does surface area, simply because there is always going to be more of it in any given room.
                        </p>

